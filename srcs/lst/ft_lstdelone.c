#include <stdlib.h>

#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	del((*alst)->content, (*alst)->content_size);
	if ((*alst)->content != NULL)
		free((*alst)->content);
	if (*alst != NULL)
		free(*alst);
	*alst = NULL;
}
